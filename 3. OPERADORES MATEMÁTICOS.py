# Operadores matemáticos
print(10 + 5)
print(10 - 5)
print(10 * 5)
print(10 / 4)

# división entera
print(10 // 4)

# residuo de la división
print(10 % 3)

# potencia
print(2 ** 4)


# Asignación
x = 10
x = x + 5
print(x)

y = 20
y += 5
print(y)

y = 20
y *= 5
print(y)


# Precedencia de operadores
x = 10 + 3 * 2
print(x)
